/**
 * Initialize i18n â€” load saved language or default to 'en'
 */
export function initI18n() {
    // Check URL param first (?lang=xx), then localStorage, then default
    const urlParams = new URLSearchParams(location.search);
    const urlLang = urlParams.get('lang');
    const saved = localStorage.getItem(STORAGE_KEY);
    if (urlLang && translations[urlLang]) {
        currentLang = urlLang;
        localStorage.setItem(STORAGE_KEY, urlLang);
    } else {
        currentLang = (saved && translations[saved]) ? saved : 'en';
    }
    applyTranslations();
}

/**
 * Get current language code
 */
export function getLang() {
    return currentLang;
}

/**
 * Set language and persist
 */
export function setLang(lang) {
    if (!translations[lang]) return;
    currentLang = lang;
    localStorage.setItem(STORAGE_KEY, lang);
    applyTranslations();
}

/**
 * Get a translated string by key, with optional interpolation.
 * @param {string} key
 * @param {Object} [params] - e.g. { name: 'foo', count: 3 }
 * @returns {string}
 */
export function t(key, params = {}) {
    let str = translations[currentLang]?.[key] || translations.en[key] || key;
    for (const [k, v] of Object.entries(params)) {
        str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), v);
    }
    return str;
}

/**
 * Apply translations to all elements with data-i18n attributes.
 * Supports:
 *   data-i18n="key"           â†’ textContent
 *   data-i18n-tooltip="key"   â†’ data-tooltip attribute
 *   data-i18n-title="key"     â†’ title attribute
 *   data-i18n-aria="key"      â†’ aria-label attribute
 *   data-i18n-placeholder="key" â†’ placeholder attribute
 */
export function applyTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        el.textContent = t(el.dataset.i18n);
    });
    document.querySelectorAll('[data-i18n-tooltip]').forEach(el => {
        el.setAttribute('data-tooltip', t(el.dataset.i18nTooltip));
    });
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
        el.title = t(el.dataset.i18nTitle);
    });
    document.querySelectorAll('[data-i18n-aria]').forEach(el => {
        el.setAttribute('aria-label', t(el.dataset.i18nAria));
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        el.placeholder = t(el.dataset.i18nPlaceholder);
    });

    // Update html lang attribute (all remaining locales are LTR)
    const meta = LANG_META[currentLang] || LANG_META.en;
    document.documentElement.lang = meta.bcp47;

    // Update SEO meta tags
    _updateSeoMeta();
}

/**
 * Update SEO meta tags based on current language
 */
function _updateSeoMeta() {
    const meta = LANG_META[currentLang] || LANG_META.en;
    const title = t('seo.title');
    const desc = t('seo.description');
    const baseUrl = location.origin + location.pathname;

    // Title
    document.title = title;

    // Meta description
    _setMeta('name', 'description', desc);

    // Open Graph
    _setMeta('property', 'og:title', title);
    _setMeta('property', 'og:description', desc);
    _setMeta('property', 'og:locale', meta.ogLocale);

    // Canonical + og:url (use ?lang= scheme for non-default)
    const langUrl = currentLang === 'en' ? baseUrl : `${baseUrl}?lang=${currentLang}`;
    const canonical = document.querySelector('link[rel="canonical"]');
    if (canonical) canonical.href = langUrl;
    _setMeta('property', 'og:url', langUrl);

    // OG alternate locales
    document.querySelectorAll('meta[property="og:locale:alternate"]').forEach(el => el.remove());
    for (const code of SUPPORTED_LANGS) {
        if (code === currentLang) continue;
        const altMeta = document.createElement('meta');
        altMeta.setAttribute('property', 'og:locale:alternate');
        altMeta.content = LANG_META[code].ogLocale;
        document.head.appendChild(altMeta);
    }

    // Twitter
    _setMeta('name', 'twitter:title', title);
    _setMeta('name', 'twitter:description', desc);

    // hreflang alternates
    document.querySelectorAll('link[rel="alternate"][hreflang]').forEach(el => el.remove());
    for (const code of SUPPORTED_LANGS) {
        const link = document.createElement('link');
        link.rel = 'alternate';
        link.hreflang = LANG_META[code].bcp47;
        link.href = code === 'en' ? baseUrl : `${baseUrl}?lang=${code}`;
        document.head.appendChild(link);
    }
    // x-default
    const xd = document.createElement('link');
    xd.rel = 'alternate';
    xd.hreflang = 'x-default';
    xd.href = baseUrl;
    document.head.appendChild(xd);

    // JSON-LD structured data
    _updateJsonLd(title, desc, langUrl);
}

function _setMeta(attr, name, content) {
    let el = document.querySelector(`meta[${attr}="${name}"]`);
    if (!el) {
        el = document.createElement('meta');
        el.setAttribute(attr, name);
        document.head.appendChild(el);
    }
    el.content = content;
}

function _updateJsonLd(title, desc, url) {
    let script = document.getElementById('jsonld-webapp');
    if (!script) {
        script = document.createElement('script');
        script.id = 'jsonld-webapp';
        script.type = 'application/ld+json';
        document.head.appendChild(script);
    }
    script.textContent = JSON.stringify({
        '@context': 'https://schema.org',
        '@type': 'WebApplication',
        'name': title,
        'description': desc,
        'url': url,
        'applicationCategory': 'DeveloperApplication',
        'operatingSystem': 'Any',
        'browserRequirements': 'Requires JavaScript',
        'offers': { '@type': 'Offer', 'price': '0', 'priceCurrency': 'USD' },
        'inLanguage': LANG_META[currentLang]?.bcp47 || 'en'
    });
}

